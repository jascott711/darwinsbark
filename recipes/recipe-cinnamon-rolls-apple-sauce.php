					<h2>Directions</h2>
					<h3>Rolls: </h3>
					<ul>
						<li>Pre-heat oven to 400°.</li>
						<li>Slice <strong>puff pastry</strong> in 4 strips.</li>
						<li>Coat with <strong>brown sugar</strong> and sprinkle over <strong>cinnamon</strong>.</li>
						<li>Roll and place on <strong>baking sheet</strong>.</li>
						<li>Bake for 15 minutes at 400°.</li>
					</ul>
					<h3>Sauce: </h3>
					<ul>
						<li>Over medium heat pour the <strong>butter</strong> into a <strong>sauce pan</strong>.</li>
						<li>Mix in the <strong>apples, brown sugar</strong> and <strong>cinnamon</strong>.</li>
						<li>Heat for about 15 mins, turn on low and let sit.</li>
					</ul>
					<h3>Icing: </h3>
					<ul>
						<li>Combine <strong>butter</strong> and <strong>3/4 cup of the icing sugar</strong> in <strong>a large mixing bowl</strong>.</li>
						<li>Pour in the <strong>milk</strong> and <strong>vanilla extract</strong>. Stir.</li>
						<li>Add more <strong>icing sugar</strong> until the icing is the desired consistency.</li>
