						<li>Add <strong>lemon juice</strong> while whisking, until smooth.</li>
						<li>Pour mixture on top of crust, carefully around the edges to not overflow.</li>
						<li>Bake (at 350°) for 15 minutes.</li>
						<li>Cool for 1 hour.</li>
					</ul>
